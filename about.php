  <h1>Welcome to Embrace Creatives</h1>
  <br>
  <h2>Our Story</h2>
  <br>
<p>Seeing the need for a B2B networking platform for artists and industry professionals, in June of 2018 Andrea Bogart, artist and business expert launched 
Embrace Creatives, a caring, business ecosystem uniting artists and commercial buyers.  Soon after, artists, designers, galleries, business experts and non-
profits from around the country began to join showcasing their art, finding opportunities and connecting with their peers and buyers.</p>

<h4>Then something amazing happened.</h4>

<p>People started asking, “How can I help you?” </p>

<p></p>When enough people signed up to become a part of her exciting vision, Bogart realized that she had tapped into something bigger than she had expected. 
Of course Artists want to sell and learn business skills but they desire more than that. Artists crave community, peer support and a clear way to affect positive change in their community. 
So using the scale of an online network paired with in-person and virtual events, Andrea is igniting an empowerment movement for Artists through her Embrace Creatives ecosystem.</p>

<p>Andrea, her Champions, and Chapter Directors are thoughtfully developing a grassroots movement to strengthen our art business ecosystem. 
EC is mindfully growing from city-to-city, state-to-state.</p>

<p>However, Bogart knows that merely opening up pathways to commercial opportunities isn’t enough.  
Artists need business skills and step-by-step guidance to learn how to work professionally with buyers and industry leaders so she added business consulting, free articles, 
and events that bring artists together with local business experts to the list of EC benefits.</p>
  
  <h4>Embrace Your Potential</h4>  

<br>

<p>A safe space to do business and sell artwork, EC is more than a typical retail and wholesale marketplace.  
Through targeted articles and educational events, our Members meet each other, build important connections and learn things like;</p>

<ul>
    <li>Which sales opportunities are trustworthy?</li>
    <li>How do I prepare for tax season?</li>
    <li>What’s the best small business insurance for me?</li>
    <li>How do I market myself and my art business?</li>
    <li>What do I need to know about copyright protection and trademarks?</li>
</ul>

<p>And our EC Champions are available to answer questions, so find them in our <a href="members">Members</a> Page  then connect with them and send a private message! </p>
<p>Embrace Creatives is where emerging and established creative businesses meet, sell, share ideas and strengthen our global arts community.</p>

